using System;

namespace OpenGlobe.Core
{
    public abstract class Disposable : IDisposable
    {
        protected virtual void Dispose(bool disposing)
        {
        }

        #region IDisposable Members

        public void Dispose()
        {
            Dispose(true);
            GC.SuppressFinalize(this);
        }

        #endregion
    }
}
